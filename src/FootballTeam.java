public class FootballTeam {
        private String name;
        private String code;
        private String country;

        // Constructor
        public FootballTeam(String name, String code, String country) {
            this.name = name;
            this.code = code;
            this.country = country;
        }

        // Getters
        public String getName() {
            return name;
        }

        public String getCode() {
            return code;
        }

        public String getCountry() {
            return country;
        }
}

